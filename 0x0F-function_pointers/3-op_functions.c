#include "3-calc.h"

/**
 *op_add - Adds two numbers
 *@a:the first number
 *@b:the second number
 *Return:The sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - substract two numbers
 *@a:the first number
 *@b:the second number
 *Return:The substraction of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - Multiply two numbers
 *@a:the first number
 *@b:the second number
 *Return:The Multiplication of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - division two numbers
 *@a:the first number
 *@b:the second number
 *Return:The division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - module two numbers
 *@a:the first number
 *@b:the second number
 *Return:The reminder of division of a and b
*/

int op_mod(int a, int b)
{
return (a % b);
}
